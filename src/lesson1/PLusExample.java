package lesson1;

public class PLusExample {

    public static void main(String[] args) {

        System.out.println(3+8);
        System.out.println(3+"8");
        System.out.println(3+8+"4");
        System.out.println(3+"8"+4);
        System.out.println(3+"8"+(2+6));

        System.out.println(4+"6"+8*9+2);
        System.out.println(4+"6"+8*9+"2");
        System.out.println("4"+(6+8)*9+"2");


    }

}
